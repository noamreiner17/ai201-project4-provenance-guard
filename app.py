@app.route("/submit", methods=["POST"])
@limiter.limit("10 per minute;100 per day")
def submit():
    data = request.get_json(silent=True) or {}
    text = data.get("text")
    creator_id = data.get("creator_id")

    # Validate the request.
    if not text or not isinstance(text, str) or not text.strip():
        return jsonify({"error": "Field 'text' is required and must be a non-empty string."}), 400
    if not creator_id or not isinstance(creator_id, str):
        return jsonify({"error": "Field 'creator_id' is required and must be a string."}), 400

    content_id = str(uuid.uuid4())

    # Signal 1 — Groq LLM.
    signal_1 = groq_signal(text)
    llm_score = signal_1["score"]

    # Signal 2 — Stylometric Analysis.
    signal_2 = stylometric_signal(text)
    stylometric_score = signal_2["score"]

    # Confidence scoring: both signals contribute equally (see planning.md).
    confidence = round((llm_score + stylometric_score) / 2, 4)
    attribution, label = classify(confidence)

    # Write a structured entry to the audit log, capturing both signal scores.
    log_event({
        "event": "classification",
        "content_id": content_id,
        "creator_id": creator_id,
        "attribution": attribution,
        "confidence": confidence,
        "llm_score": llm_score,
        "stylometric_score": stylometric_score,
        "label": label["label"],
        "status": "classified",
        "appeal_filed": False,
    })

    return jsonify({
        "content_id": content_id,
        "attribution": attribution,
        "confidence": confidence,
        "label": label["label"],
        "label_detail": label["label_detail"],
        "llm_score": llm_score,
        "stylometric_score": stylometric_score,
    })


def find_classification(content_id):
    """Return the original classification log entry for content_id, or None."""
    for entry in reversed(read_log(limit=10000)):
        if entry.get("content_id") == content_id and entry.get("event") == "classification":
            return entry
    return None


@app.route("/appeal", methods=["POST"])
def appeal():
    data = request.get_json(silent=True) or {}
    content_id = data.get("content_id")
    reasoning = data.get("creator_reasoning")

    # Validate the request.
    if not content_id or not isinstance(content_id, str):
        return jsonify({"error": "Field 'content_id' is required and must be a string."}), 400
    if not reasoning or not isinstance(reasoning, str) or not reasoning.strip():
        return jsonify({"error": "Field 'creator_reasoning' is required and must be a non-empty string."}), 400

    # Verify the submission exists before accepting the appeal.
    original = find_classification(content_id)
    if original is None:
        return jsonify({"error": f"No submission found for content_id '{content_id}'."}), 404

    # Log the appeal alongside the original classification decision, including the
    # creator's reasoning so a reviewer has full context.
    log_event({
        "event": "appeal",
        "content_id": content_id,
        "creator_id": original.get("creator_id"),
        "attribution": original.get("attribution"),
        "confidence": original.get("confidence"),
        "llm_score": original.get("llm_score"),
        "stylometric_score": original.get("stylometric_score"),
        "label": original.get("label"),
        "appeal_reasoning": reasoning,
        "appeal_filed": True,
        "status": "under_review",
    })

    return jsonify({
        "content_id": content_id,
        "status": "under_review",
        "message": "Your appeal was received and is under review.",
    })


@app.route("/log", methods=["GET"])
def view_log():
    return jsonify({
        "entries": read_log()
    })


if __name__ == "__main__":
    app.run(port=5000, debug=True)