# The three transparency-label variants, verbatim from planning.md
# (Transparency Label Design section).
LABEL_AI = {
    "label": "Likely AI-Generated",
    "label_detail": (
        "Our system found strong evidence that this content was generated by "
        "artificial intelligence. Confidence: High."
    ),
}
LABEL_HUMAN = {
    "label": "Likely Human-Written",
    "label_detail": (
        "Our system found strong evidence that this content was written by a "
        "human author. Confidence: High."
    ),
}
LABEL_UNCERTAIN = {
    "label": "Uncertain Result",
    "label_detail": (
        "Our system could not confidently determine whether this content was "
        "AI-generated or human-written. This result should not be considered "
        "definitive and may require human review."
    ),
}


def generate_label(confidence):
    """Map a confidence score (0.0 human .. 1.0 AI) to the transparency label.

    Returns the full label variant (title + detail) so the text shown to the
    user changes with the score. The three variants are the ones designed in
    planning.md. Returns: dict with "label" and "label_detail".
    """
    if confidence <= 0.45:
        return LABEL_HUMAN
    if confidence <= 0.54:
        return LABEL_UNCERTAIN
    return LABEL_AI


def classify(confidence):
    """Map a confidence score (0.0 human .. 1.0 AI) to an attribution and label.

    Mirrors the Uncertainty Representation table in planning.md. The attribution
    has five levels; the transparency label collapses to the three variants
    returned by generate_label().
    """
    if confidence <= 0.25:
        attribution = "human"
    elif confidence <= 0.45:
        attribution = "likely_human"
    elif confidence <= 0.54:
        attribution = "uncertain"
    elif confidence <= 0.75:
        attribution = "likely_ai"
    else:
        attribution = "ai"
    return attribution, generate_label(confidence)

